# Déclaration de variables en Python
a = 20
b = "maman"

# Affichage dans la console
print(f"La valeur de a est : {a}")
print(f"La valeur de b est : {b}")

# Exemples de conditions
if a > 10:
    print("a est supérieur à 10")
else:
    print("a est inférieur ou égal à 10")

# Boucle for
for i in range(5):
    print(f"i vaut : {i}")

# Fonction simple
def afficher_message(message):
    print(message)

afficher_message("Ceci est un message affiché via une fonction.")
